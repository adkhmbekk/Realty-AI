export function labelHelpers(lang: Lang, t: (k: string) => string) {
  return {
    typeLabel(v?: string | null) {
      if (!v) return v ?? "";
      const i = OBJ_TYPE_VALUES.indexOf(v);
      return i >= 0 ? (OBJ_TYPE_LABELS[lang] || OBJ_TYPE_LABELS.ru)[i] : v;
    },
    condLabel(v?: string | null) {
      if (!v) return v ?? "";
      const i = OBJ_COND_VALUES.indexOf(v);
      return i >= 0 ? (OBJ_COND_LABELS[lang] || OBJ_COND_LABELS.ru)[i] : v;
    },
    faLabel(v?: string | null): string | null {
      return v && FA_LABEL_KEYS[v] ? t(FA_LABEL_KEYS[v]) : null;
    },
    statusLabel(s: string) {
      const map: Record<string, string> = {
        active: t("statusActive"),
        deposit: t("statusDeposit"),
        sold: t("statusSold"),
      };
      return map[s] || s;
    },
    roleLabel(role?: string | null) {
      const k = "role_" + role;
      const l = I18N[lang];
      return l && l[k] ? l[k] : role || t("notSet");
    },
  };
}
